    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // TODO add your handling code here:
        cardLayout.show(mainContainer, "mainPanel");
        unameStatus.setText("");
        passStatus.setText("");
        String username = unameTextField.getText();
        String password = passField.getText();
        List<Kasir> kasirs = this.minimarket.getCashiers();
    	
    	boolean isLogin = false;
    	boolean isUsernameExist = false;
    	for(Kasir kasir : kasirs) {
            if(kasir.getUsernameKasir().equalsIgnoreCase(username)) {
                if(kasir.getUsernameKasir().equalsIgnoreCase(username) &&
                        kasir.getPasswordKasir().equalsIgnoreCase(password)) {
                    isUsernameExist = true;
                    isLogin = true;
                    chasier = kasir;
                    break;
                } else {
                    isUsernameExist = true;
                }
            }
    		
    		
    	}
    	
    	if(!isLogin) {
            if(!isUsernameExist) {
                unameStatus.setText("Username tidak ada");
            } else {
                passStatus.setText("Password salah");
            }
    	}
        else{
            //change card
            cardLayout.show(mainContainer, "mainMenuCard");
            mainMenuCardLayout.show(mainMenuCardContainer, "firstPanelMainMenuCard");
        }
        restockComboBox.setModel(new DefaultComboBoxModel(listProduk));
        hapusProdukComboBox.setModel(new DefaultComboBoxModel(listProduk));
    }//GEN-LAST:event_loginButtonActionPerformed

