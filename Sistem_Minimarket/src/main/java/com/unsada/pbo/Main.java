    public void setTableKeranjang(){
        String[][] data = new String[this.transaksi.getKeranjang().size()][3];
        String[] kolom = {"Nama Produk", "Jumlah", "Total Harga"};
        int index = 0;
        for(Map.Entry<Produk, Penjualan> entry: this.transaksi.getKeranjang().entrySet()){
            String namaProduk = entry.getKey().getNamaProduk();
            int jumlah = entry.getValue().getJumlah_produk();
            double total = entry.getValue().getTotal_harga();
            data[index][0] = entry.getKey().getNamaProduk();
            data[index][1] = ""+entry.getValue().getJumlah_produk();
            data[index][2] = "Rp."+entry.getValue().getTotal_harga();
            index++;
        }
        keranjangTable.setModel(new DefaultTableModel(data,kolom));
    }
